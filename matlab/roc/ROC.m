function [fp tp th] = ROC(n,p)
%This function calculates values for the ROC.
%Needs to build the mex file roc_for.c

%Normalize scores in [0,1]
if(size(n,1) > size(n,2))
    n=n';
end

if(size(p,1) > size(p,2))
    p=p';
end


th = unique([n, p]);
 
[fp tp] = roc_for(th,n,p);


fp=fp/numel(n);
tp=tp/numel(p);



